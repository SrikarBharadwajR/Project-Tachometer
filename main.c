void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) {
	SSD1306_Puts("hi", &Font_7x10, 1);
	HAL_Delay(200);
	SSD1306_UpdateScreen();
	if (state == IDLE) {
		T1 = TIM4->CCR4;
		TIM4_OVC = 0;
		state = DONE;
	} else if (state == DONE) {
		T2 = TIM4->CCR4;
		ticks = (T2 + (TIM4_OVC * 65536)) - T1;
		state = IDLE;
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	TIM4_OVC++;
	SSD1306_GotoXY(0, 0);
	SSD1306_Puts("hi", &Font_7x10, 1);
	HAL_Delay(200);
	SSD1306_UpdateScreen();
}

